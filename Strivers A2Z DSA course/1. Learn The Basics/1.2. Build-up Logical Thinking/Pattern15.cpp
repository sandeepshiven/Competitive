class Solution {
  public:
    void printTriangle(int n) {
        
        char c = 'A';
        for(int i=n; i>0; i--){
            for(int j=0; j<i; j++){
                cout << (char)(c+j);
            }
            cout << '\n';
        }

    }
};

//{ Driver Code Starts.

int main() {
    int t;
    cin >> t;
    while (t--) {
        int n;
        cin >> n;

        Solution ob;
        ob.printTriangle(n);
    }
    return 0;
}
